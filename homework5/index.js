  email: /^[\w\.-]+@[\w\.-]+(\.[A-Za-z\d]{2,6}){1,2}$/,
  url: /^(http)s?:\/\/(\w+\.){2,}\w+(:\d{2,4})?(\/\w+)*\/?$/,
  ipv4: /^((\d{0,2}|1\d{2}|2[0-4]\d|25[0-5])\.){3}(\d{0,2}|1\d{2}|2[0-4]\d|25[0-5])$/,
  idcard: /^\d{6}(1[89]|20)?\d{2}(0[1-9]|1[0-2])(0[1-9]|[12]\d|3[0-1])\d{3}[\dXx]$/,
